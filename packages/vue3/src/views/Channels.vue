<template>
  <h-tabs xclass="mb-4 bg-bgColor-500" with-container>
    <h-tab :active="category === 0" @click="category = 0">
      {{ $t("views.channels.tabs.Vtuber") }}
    </h-tab>
    <h-tab :active="category === 1" @click="category = 1">
      {{ $t("views.channels.tabs.Subber") }}
    </h-tab>
    <h-tab :active="category === 2" @click="category = 2">
      {{ $t("views.channels.tabs.Favorites") }}
    </h-tab>
    <h-tab :active="category === 3" @click="category = 3">
      {{ $t("views.channels.tabs.Blocked") }}
    </h-tab>
    <template v-if="category !== Tabs.BLOCKED" #filters>
      <h-menu strategy="fixed" placement="bottom-end">
        <template #activator="activator">
          <button class="dropdownLabelBtn" v-bind="activator.props">
            {{ currentSortValue.text }}
            <h-icon class="i-heroicons:chevron-down -mr-1 ml-1 h-4 w-4" />
          </button>
        </template>
        <template #default>
          <ul
            tabindex="0"
            class="dropdown-content menu rounded-box z-50 bg-bgColor p-2 text-sm shadow"
            style="min-width: 10rem"
          >
            <li
              v-for="(item, index) in sortOptions"
              :key="index"
              link
              role="none"
              @click="sort = item.value"
            >
              <a role="menuitem">{{ item.text }}</a>
            </li>
          </ul>
        </template>
      </h-menu>
    </template>
  </h-tabs>
  <!-- Toggle of Card or Row view -->
  <!-- <div class="btn btn-outline" @click="cardView = !cardView">
          <v-icon>
            {{ cardView ? mdiViewModule : mdiViewList }}
          </v-icon>
        </div> -->

  <div class="container mx-auto px-2" style="min-height: 70vh">
    <div class="container">
      <channel-list
        v-if="category !== Tabs.BLOCKED"
        :channels="channelList"
        :query="query"
        :variant="cardView ? 'card' : 'list'"
        :grouping="currentSortValue.value"
      >
        <template #default="{ channelData: channel, channelActions }">
          <!-- <div class="h-0 basis-full" /> -->
          <div
            class="col-span-2 flex w-full flex-row flex-nowrap gap-1 border-t border-solid border-t-bgColor-700 bg-bgColor-500"
          >
            <h-btn
              :title="
                channelActions.isFav.value
                  ? $t('component.channelSocials.removeFromFavorites')
                  : $t('component.channelSocials.addToFavorites')
              "
              class="btn-ghost w-32 shrink"
              :icon="
                channelActions.isFav.value
                  ? 'i-mdi:heart text-red-500'
                  : 'i-mdi:heart-outline'
              "
              @click.prevent.stop="channelActions.toggleFav(channel)"
            />
            <h-btn
              title="Button"
              class="btn-ghost w-3/12 shrink"
              :icon="icons.youtube"
              :href="`https://youtube.com/channel/${channel.id}`"
            />
            <h-btn
              title="Button"
              class="btn-ghost w-3/12 shrink"
              :class="{
                'btn-disabled bg-inherit opacity-20': !channel.twitter,
              }"
              :icon="icons.twitter"
              :href="channel.twitter || '#'"
            />
            <h-btn
              title="Button"
              class="btn-ghost w-3/12 shrink"
              :class="{ 'btn-disabled bg-inherit opacity-20': !channel.twitch }"
              :icon="icons.twitch"
              :href="channel.twitch || '#'"
            />
            <h-btn
              title="Button"
              class="btn-ghost w-1/12 shrink opacity-50"
              icon="i-mdi:block-helper"
            />
          </div>
        </template>
      </channel-list>
      <blocked-list v-else />
      <!-- Static channel list with no loading for locally stored blocked/favorites list -->
      <!-- <ChannelList
        :channels="channelList"
        include-video-count
        :grouped="sort === 'group'"
        :card-view="cardView"
        :show-delete="category === Tabs.BLOCKED"
      /> -->
    </div>
    <!-- Favorites specific view items: -->
    <template v-if="category === Tabs.FAVORITES">
      <div v-if="!favorites.data.value || favorites.data.value?.length === 0">
        {{ $t("views.channels.favoritesAreEmpty") }}
      </div>
    </template>
    <!-- Blocked list specific view items -->
    <template v-if="category === Tabs.BLOCKED">
      <div
        v-if="
          !settings.blockedChannels || settings.blockedChannels.length === 0
        "
      >
        {{ $t("views.channels.blockedAreEmpty") }}
      </div>
    </template>
  </div>
</template>

<script lang="ts">
import { CHANNEL_TYPES } from "@/utils/consts";

import { localSortChannels } from "@/utils/functions";
import { useLangStore, useSiteStore } from "@/stores";
import { useI18n } from "vue-i18n";
import { useFavoritesList } from "@/services/favorites";
import { useSettingsStore } from "@/stores/settings";
import useOrgRouteParamSync from "@/hooks/common/useOrgRouteParamSync";
export default defineComponent({
  name: "Channels",
  //   metaInfo() {
  //     const vm = this;
  //     return {
  //       get title() {
  //         return `${vm.$t("component.mainNav.channels")} - Holodex`;
  //       },
  //     };
  //   },
  setup() {
    const lang = useLangStore();
    const site = useSiteStore();
    const settings = useSettingsStore();
    const { t } = useI18n();

    const sort = ref("subscribers");

    const favorites = useFavoritesList();
    const org = useOrgRouteParamSync();

    return { site, lang, t, sort, favorites, settings, org };
  },
  data() {
    return {
      // perPage: 25,
      identifier: +new Date(),
      // freeze object to stop Vue from creating watchers (small optimization)
      Tabs: {
        VTUBER: 0,
        SUBBER: 1,
        FAVORITES: 2,
        BLOCKED: 3,
      },
      category: 0,
      defaultSort: "subscribers",
      cardView: false,
    };
  },
  computed: {
    sortOptions(): { text: string; value: string; query_value: any }[] {
      return [
        {
          text: this.t("views.channels.sortOptions.subscribers"),
          value: "subscribers",
          query_value: {
            sort: "subscriber_count",
            order: "desc",
          },
        },
        ...((this.category === this.Tabs.VTUBER ||
          this.category === this.Tabs.FAVORITES) &&
        this.site.currentOrg.name !== "All Vtubers"
          ? [
              {
                text: this.$t("views.channels.sortOptions.group"),
                value: "group",
                query_value: {
                  sort: "suborg",
                  order: "asc",
                },
              },
            ]
          : []),
        ...(this.site.currentOrg.name === "All Vtubers"
          ? [
              {
                text: this.$t("views.channels.sortOptions.org"),
                value: "org",
                query_value: {
                  sort: "org",
                  order: "asc",
                },
              },
            ]
          : []),
        {
          text: this.$t("views.channels.sortOptions.videoCount"),
          value: "video_count",
          query_value: {
            sort: "video_count",
            order: "desc",
          },
        },
        ...(this.category === this.Tabs.VTUBER ||
        this.category === this.Tabs.FAVORITES
          ? [
              {
                text: this.$t("views.channels.sortOptions.clipCount"),
                value: "clip_count",
                query_value: {
                  sort: "clip_count",
                  order: "desc",
                },
              },
            ]
          : []),
        ...(this.category === this.Tabs.VTUBER ||
        this.category === this.Tabs.SUBBER
          ? [
              {
                text: "Recently Added",
                value: "recently_added",
                query_value: {
                  sort: "created_at",
                  order: "desc",
                },
              },
            ]
          : []),
      ];
    },
    channelList() {
      if (this.category === this.Tabs.FAVORITES) return this.sortedFavorites;
      if (this.category === this.Tabs.BLOCKED)
        return this.settings.blockedChannels;
      return undefined;
    },
    currentSortValue() {
      // const x = this.category;
      return (
        this.findSortValue(this.sort) ||
        (this.findSortValue(this.defaultSort) as {
          text: string;
          value: string;
          query_value: any;
        })
      );
    },
    sortedFavorites() {
      console.log("sort favorites", this.favorites.data.value);
      return localSortChannels(
        this.favorites.data.value || [],
        this.currentSortValue.query_value,
      );
    },
    isLoggedIn() {
      return !!this.site.user;
    },
    query() {
      const type =
        this.category === this.Tabs.SUBBER
          ? CHANNEL_TYPES.SUBBER
          : CHANNEL_TYPES.VTUBER;
      return {
        type,
        ...(type === CHANNEL_TYPES.VTUBER && {
          org: this.org.name,
        }),
        ...this.currentSortValue.query_value,
      };
    },
  },
  watch: {},
  methods: {
    // changing category also changes sort, which will cause this to trigger twice
    findSortValue(sort: string) {
      return this.sortOptions.find((opt) => opt.value === sort);
    },
  },
});
</script>

